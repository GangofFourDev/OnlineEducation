QString utf82unicode(std::string value)
{
	QTextCodec *utf8 = QTextCodec::codecForName("UTF-8");
	QString str = utf8->toUnicode(value.c_str());
	return str;
}

std::string unicode2utf8(QString value)
{
	QTextCodec *utf8 = QTextCodec::codecForName("UTF-8");
	QByteArray ba = utf8->fromUnicode(value);
	return ba.data();
}

